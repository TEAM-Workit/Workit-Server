package workit.util;

import lombok.AllArgsConstructor;

@AllArgsConstructor
public enum StatusCode {
    OK(20000),
    CREATED(20100),

    // 400
    BAD_REQUEST(40000),
    NULL_VALUE(40001),
    USER_NOT_FOUND(40002),
    INVALID_SOCIAL_TYPE(40003),
    LOGIN_FAILED(40004),
    FAILED_VALIDATE_APPLE_LOGIN(40005),
    INVALID_DATE_TYPE(40006),
    NO_ABILITIES(40007),
    INVALID_WORK_DESCRIPTION_LENGTH(40008),
    INVALID_WORK_TITLE_LENGTH(40009),
    NOT_USER_WORK(40010),
    INVALID_PROJECT_TITLE_LENGTH(40011),
    ALREADY_EXIST_PROJECT_TITLE(40012),
    NOT_USER_PROJECT(40013),

    // 401
    UNAUTHORIZED(40100),

    // 404
    WORK_NOT_FOUND(40400),
    PROJECT_NOT_FOUND(40401),
    ABILITY_NOT_FOUND(40402),


    INTERNAL_SERVER_ERROR(50000);

    private final int value;

    public int getValue() {
        return value;
    }
}
